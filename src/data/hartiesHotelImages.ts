// Helper function to get all images for a specific Harties hotel
export function getHartiesHotelImages(
  tier: 'budget' | 'affordable',
  capacity: '2_sleeper' | '4_sleeper',
  optionNumber: number
): string[] {
  if (capacity === '2_sleeper') {
    const images = tier === 'budget' 
      ? hartiesBudget2SleeperImages[optionNumber - 1]
      : hartiesAffordable2SleeperImages[optionNumber - 1];
    return images || [];
  }
  // 4-sleeper images not yet generated - return empty for now
  return [];
}

// Helper function to get primary image for a Harties hotel
export function getHartiesHotelPrimaryImage(
  tier: 'budget' | 'affordable',
  capacity: '2_sleeper' | '4_sleeper',
  optionNumber: number
): string | null {
  const images = getHartiesHotelImages(tier, capacity, optionNumber);
  return images.length > 0 ? images[0] : null;
}
